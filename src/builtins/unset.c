#include "../../includes/minishell.h"
#include "../../includes/builtins.h"

void ft_unset(t_env **env, char *key)
{
	t_env   *tmp = *env;
	t_env   *prev = NULL;

	while (tmp)
	{
		if (ft_strcmp(tmp->key, key) == 0)
		{
			if (prev)
				prev->next = tmp->next;
			else
				*env = tmp->next;
			free(tmp->key);
			free(tmp->value);
			free(tmp);
			return;
		}
		prev = tmp;
		tmp = tmp->next;
	}
}
